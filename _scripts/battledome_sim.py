import random

def sample(lst):
    # Algorithm is to
    # 1. Shuffle the list
    # 2. Sort the list (ties are broken via stable sorting)
    # 3. Pick random integer 1 to total
    # 4. Sum list until current sum >= sampled value then give that item.

    # List above needs to be scaled by 100
    random.shuffle(lst)
    lst.sort(key=lambda pair: pair[1])
    items = [(name, v*100) for name, v in lst]
    total = sum(v for name, v in items)
    # Assume we take the floor to guarantee that the last item ends the algorithm
    sample = random.randint(1, int(total))
    tot = 0
    for name, v in items:
        tot += v
        if tot >= sample:
            return name
    print('This should not happen')
    return items[-1][0]

